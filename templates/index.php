Gothic 3: Forsaken Gods[править | править вики-текст]
Основная статья: Gothic 3: Forsaken Gods
Gothic 3: Forsaken Gods — независимое от оригинальной игры дополнение к Готике 3. Разработкой проекта занималась индийская компания Trine Games, релиз состоялся 21 ноября 2008 года. Фактически данное дополнение не признается фанатами как часть серии.

Действие игры происходит в королевстве Миртана спустя два года после событий Готики 3. Из оригинальных трёх концовок предыдущей части взята в основу концовка за Ксардаса. По сюжету дополнения, герою вновь предстоит спасти (а, возможно, и сплотить) королевство Миртана в ожесточенных схватках и поединках, выбирая мораль праведного выбора или лютой ожесточенности.

Что касается самой основы геймплея, то тут сложно сказать о достоинствах тех 15-20 часов, что предлагаются игроку. Наличие множества ошибок (невидимые NPC, статичные и недвижимые модели птиц, невыполнимые задания и прочее) предполагает заведомо негативное впечатление от первого знакомства (даже при обновлении патчами).

Выход на консоли[править | править вики-текст]
В конце 2006 года в интернет просочилась информация о том, что Microsoft вела переговоры с Piranha Bytes по поводу портирования Gothic 3 на консоль Xbox 360. После этого многие фанаты серии Gothic ждали релиза игры на Xbox 360, однако он так и не состоялся. Возможно, отмена релиза была связана с трудностями с переносом игры на консоль (ведь на время выхода игра была полна недоработок, исправленных только после выходов патчей).
	
